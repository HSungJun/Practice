import java.lang.*;

// Convention (관습,관례)
// {} 및 첫글자는 대문자- 글로벌 컨벤션
// GNU Style - 한칸 들여쓰기 중괄호 위치 통일
// BSD Style - 들여쓰기 없이 아래로 중괄호 내려치기
// K&R Style - 중괄호 오픈지점을 오른쪽으로 옮김


public class Exam_01 // 파일명과 똑같이
{
	public static void main(String[] ar) //초기 실행위치
	{
		System.out.println("Hello World");
		System.out.println("Java Study! 한글도 되나?");
		System.out.println(10);
		System.out.println(3.14);
		System.out.println("A"); 
	// 자바에서 문자열을 표현할 때에는 "" 사용
	// 자바에서 한글자를 표현할 때에는 '' 사용
	// 자바에서 숫자를 표현할 때에는 no quote(따옴표 미사용)
		
	}
}



