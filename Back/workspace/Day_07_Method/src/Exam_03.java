
public class Exam_03 {

	public static void func() {
		System.out.println();
	}
	
	public static void func(int a) {
		System.out.println();
	}
	
	// Method Overloading
	//메소드의 매개변수의 개수 또는 타입에 따라 중복된 메서드 이름도 구분하여 사용할 수 있는 기법
	
	
	public static void main(String[] args) {
		
		System.out.println();
		//인자값이 없어 아래의 func로 자바가 인식
	}
	
}
